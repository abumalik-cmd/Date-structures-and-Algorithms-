/*
У вас есть набор целых чисел s, который изначально содержит все числа от 1 до n. К сожалению, 
из-за какой-то ошибки одно из чисел в s было продублировано другим числом из набора,
что привело к повторению одного числа и потере другого числа.

Вам выдается целочисленный массив, nums представляющий состояние данных этого набора после ошибки.

Найдите число, которое встречается дважды, и число, которое отсутствует, и верните их в виде массива.

 

Пример 1:

Входные данные: nums = [1,2,2,4]
Выходные данные: [2,3]  */


class Solution {
    public int[] findErrorNums (int[] nums) {
        int duplicate = -1;
        int missing = -1;
        
        for (int i = 1; i <= nums.length; i++) {
            int count = 0;
            
            // Считаем, сколько раз число i встречается в массиве
            for (int j = 0; j < nums.length; j++) {
                if (nums[j] == i) {
                    count++;
                }
            }
            
            // Если встречается 2 раза - это дубликат
            if (count == 2) {
                duplicate = i;
            }
            // Если не встречается ни разу - это пропущенное число
            else if (count == 0) {
                missing = i;
            }
        }
    }
}